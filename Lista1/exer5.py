"""
O número de bactérias em um meio duplica de hora em hora. Se,
inicialmente, existem 8 bactérias, qual será o número de bactérias ao fim
de 10 horas?

"""

numBac = 8
t = 10

novo_numBac = numBac * 2**t
print("Numero de bacterias : {}" .format(novo_numBac))
print(f"Numero de bacterias : {novo_numBac} ")
